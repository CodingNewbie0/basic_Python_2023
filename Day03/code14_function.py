# 함수
# 함수정의 - 실행X
def add(x, y):
    result = x + y 
    return result

def sub(x, y):
    result = x - y
    return result

def mul(x, y):
    result = x * y
    return result

def div(x, y):
    result = x // y
    return result

def mok(x, y):
    result = x % y
    return result

# 함수호출
val = add(1024, 5)
print(val)

val = sub(1024, 100)
print(val)

val = mul(1024, 2)
print(val)

val = div(1024, 512)
print(val)

val = mok(1024, 37)
print(val)

